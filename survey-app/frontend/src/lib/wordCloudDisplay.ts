/** Как в backend/functions/lib/text-word-cloud.js — одинаковая нормализация и стоп-лист. */
function normalizeWordCloudToken(s: string): string {
  return String(s || '')
    .normalize('NFKC')
    .toLowerCase()
    .trim()
    .replace(/\uFEFF/g, '')
    .replace(/ё/g, 'е');
}

